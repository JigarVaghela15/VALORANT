<br/>
Occasionally, a patch may become available to download but the server itself will still be down for maintenance for a few more hours.</b><br/><br/>
As Riot is a US-based company, patch schedules can be affected by any federal holidays that occur in the country. In most cases, these holidays will push an upcoming patch release to later by one day for a Wednesday release. Some larger holiday periods can cause patches during this time to be longer than two weeks, with a three-week patch occurring over Thanksgiving, and a five-week patch occurring over Christmas and the New Year. Three to four-week patches can also occur when the game undergoes an engine update and also during Riot's week-long summer break around July-August each year. In some cases for these longer patches that occur in the middle of an episode, a patch number may be "skipped" to reflect this.
