<h1><b>Acts</b></h1>
An Act is a competitve time frame in VALORANT. There are three acts in every episode, and each act contains around four or five patches (each about two weeks long), meaning that one act is about two months long.<br><br>
New acts are some of the biggest updates in VALORANT. In addition to major content releases like agents and maps, each act contains a new Battle Pass and changes to Competitive. Players get an Act Rank badge on their player card depending on the highest rank they reached in the previous act, and they must play a placement game to earn a rank for this act that will place them back at their previous rank, though the rank placement may shift slightly. Each act will give players a new Act Rank graphic to complete, and their previous Act Rank will be stored in their Career tab.<br><br>
Riot had initially stated that they want to release an agent at the beginning of every act and are therefore targeting about six agent releases every year, though they were also willing to be flexible. This means they would be willing to prioritize releasing other content for an act over a new agent, such as FORMATION: Act 3 being the first act to not add a new agent, with only a new map added instead. Since then, any act releasing a new map has not included a new agent along with it, and the intended frequency at which new agents are released has also been decreased.
