Players with access to the Premium track will earn additional cosmetics and RP, with the exception of player titles (all Act Pass player titles are part of the free track). These include access to three new collections of weapon skins, two of which will be Select Select Edition and one of which will be Deluxe Deluxe Edition. The Tier 50 reward for Premium Track owners will always be a knife skin, one of the most "valuable" types of weapon skins in the game. The Epilogue chapter also has set rewards; golden variants of two rewards from this pass (cosmetics eligible for golden variants are cards, gunbuddies, and sprays), unlocked at tiers 1 and 5, and three lots of Radianite Points 10, unlocked at tiers 2, 3, and 4.<br/><br/>

When a new act arrives, a new pass with new content begins and the previous pass becomes unavailable, with its weapon skins no longer being able to be obtained by any means. Once an act battle pass is no longer one of the three most recent passes to have been released, its accessory content will become eligible to appear in a player's weekly Offers in their Accessory Store, where players can unlock past items using Kingdom Credits.
