The Battle Pass is a <b>cosmetic</b> system in <b>VALORANT</b> that allows players to unlock a variety of cosmetics as they earn experience through missions and match <b>XP.</b><br/><br/>

Each <b>act</b> has its own pass, and every new act closes progress on the current pass and resetting all players back to the beginning of the new pass. All players can unlock cosmetics rewards from the act pass, but most of that pass' content can only be unlocked by paying Valorant Points1,000 to get access to the Premium track.<br/><br/>

Occasionally, additional passes are released within an act to celebrate an occasion, such as VALORANT's anniversaries.<br/>
<br/>

Every act, a new act Battle Pass is launched that lasts for the duration of the act (about two months). Each act pass has 55 tiers grouped into 11 chapters; chapters 1-10 and a final Epilogue chapter, with 5 tiers in every chapter. Players start with Tier 1 completed. The amount of XP required to complete Tier 2 is 2,000, and for every tier after that (except for Epilogue Tiers), the amount of XP required to complete that tier increases by 750:<br/><br/>

<b>XP to complete a specific tier =(Tier Number Ã— 750)+500</b><br/><br/>

Each Epilogue tier requires 36,500 XP. This means the total amount of XP required to complete the pass is 1,162,500 XP, and 980,000 XP is required to reach Chapter 10/Tier 50.<br/><br/>

There are two tracks in the act pass; Free and Premium. The free track gives all players rewards with every chapter they complete, as well as access to all Epilogue rewards. The Premium track, which costs Valorant Points1,000 to get access to, boosts Battle Pass XP gains from playing matchmade games (mission XP excluded) by 3% and gives these players additional rewards for every tier they complete. Players earn progress for tiers regardless of whether they have bought access to the Premium track or not. This means players can buy access to the Premium track weeks after the act begins to retroactively unlock the rewards they earned while on the free track. The 3% XP boost however is not applied retroactively and only takes effect for every game played after the player unlocked the Premium track. Premium track players can also automatically earn progress to reach a specific tier they haven't unlocked by paying Valorant Points300 for every tier that needs to be completed to reach the player's target. This cannot be used for Epilogue tiers.<br/><br/>

Act passes contain weapon skins, accessories (player cards, gunbuddies, sprays, player titles), and Radianite Points. The amount of each type of cosmetic in this pass can vary, but for the free track it is always the same:<br/><br/>
